#include <stdio.h>

int main()
{
    int a = 4;
    // int b = 8.5 // not recommended
    float b = 8.5;
    char c = 'a';
    int d = 45;
    printf("The value of a is %d \n", a);
    printf("The value of b is %f \n", b);
    printf("The value of b is '%c' \n", c);
    printf("The sum of a and d is %d \n", a + d);
    return 0;
}